package zone.rong.dexterity.rpg.skill.client.mixin.cultivation;

import net.minecraft.client.render.VertexConsumerProvider;
import net.minecraft.client.render.entity.EntityRenderDispatcher;
import net.minecraft.client.util.math.MatrixStack;
import net.minecraft.entity.Entity;
import net.minecraft.entity.passive.AnimalEntity;
import net.minecraft.world.WorldView;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.ModifyVariable;
import zone.rong.dexterity.api.DexterityEntityTrackers;

@Mixin(EntityRenderDispatcher.class)
public class MixinEntityRenderDispatcher {

    @ModifyVariable(method = "renderShadow", at = @At("HEAD"), name = "radius")
    private static float modifyFloat(float radius, MatrixStack stack, VertexConsumerProvider vcp, Entity entity, float opacity, float delta, WorldView world) {
        if (entity instanceof AnimalEntity) {
            return radius * entity.getDataTracker().get(DexterityEntityTrackers.General.WIDTH_MULTIPLIER);
        }
        return radius;
    }

}
